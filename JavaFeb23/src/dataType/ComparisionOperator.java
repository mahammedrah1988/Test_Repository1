package dataType;

public class ComparisionOperator {

	public static void main(String[] args) {
	
		String actual = "Google";
		String expected = "google";
		
		/** ==
		 * !=
		 * >
		 * <
		 * >=
		 * <=
		 */
		
		
		int a = 15;
		int b = 16;
		
		System.out.println(actual==expected);
		System.out.println(actual!=expected);
		System.out.println(a>b);
		System.out.println(a<b);
		
	}

}
